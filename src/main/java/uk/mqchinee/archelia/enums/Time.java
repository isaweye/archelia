package uk.mqchinee.archelia.enums;

/**
 * An enumeration representing different options for time units.
 * <p>
 * This enum provides options to represent time in either seconds or ticks.
 * </p>
 */
public enum Time {
    /**
     * Represents time measured in seconds.
     */
    IN_SECONDS,

    /**
     * Represents time measured in ticks.
     */
    IN_TICKS
}
