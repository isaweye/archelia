package uk.mqchinee.archelia.enums;

/**
 * An enumeration representing two possible ways of comparing elements.
 * <p>
 * This enum is used in various contexts to specify the attribute by which elements should be compared.
 * </p>
 * @since 1.0
 */
public enum ComparingBy {
    /**
     * Compare elements by their keys.
     */
    KEY,

    /**
     * Compare elements by their values.
     */
    VALUE
}
